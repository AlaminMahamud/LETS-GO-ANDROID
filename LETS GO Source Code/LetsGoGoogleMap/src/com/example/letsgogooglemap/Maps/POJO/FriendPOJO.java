package com.example.letsgogooglemap.Maps.POJO;

public class FriendPOJO {
	public String name;
	public String email;
	public String phone;
	public String username;
	public boolean box;
	
	public FriendPOJO(String name, String email, String phone){
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.username = username;
		this.box = false;
	}
}
